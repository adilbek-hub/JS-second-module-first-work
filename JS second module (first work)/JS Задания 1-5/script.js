/*5. Создайте программу, которая запрашивает у пользователя два целых 
числа **a** и **b**, после чего выводит на экран результаты следующих 
математических операций:
    
    - сумма a и b; 
    
    - разница между a и b;
    
    - произведение a и b; 
    
    - частное от деления a на b; 
    
    - остаток от деления a на b;  
    
    - результат возведения числа a в степень b*/

    let strNum2 = Number(prompt("Дайте значения на а"));
    let strNum3 = Number(prompt("Дайте значения на b"));
    document.write(`<b>${strNum2} + ${strNum3} = ${strNum2 + strNum3}</b><br>`);
    document.write(`<b>${strNum2} - ${strNum3} = ${strNum2 - strNum3}</b><br>`);
    document.write(`<b>${strNum2} * ${strNum3} = ${strNum2 * strNum3}</b><br>`);
    document.write(`<b>${strNum2} / ${strNum3} = ${strNum2 / strNum3}</b><br>`);
    document.write(`<b>${strNum2} % ${strNum3} = ${strNum2 % strNum3}</b><br>`);
    document.write(`<b>${strNum2} ** ${strNum3} = ${strNum2 ** strNum3}</b>`); 




        
    
    
    
  
